    void showData(String s){
         try {
             conn = (Connection) apoteknusantara.koneksi.connect(db);
             Statement st = conn.createStatement();
             ResultSet sql;
             if ( s != null){
             sql = st.executeQuery("select * from supplier where nama like '%"+cari.getText()+"%'");
             }else{
             sql = st.executeQuery("select * from supplier");
             }
             
             tabel.setModel(DbUtils.resultSetToTableModel(sql));
             conn.close();
         } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
         }
     }
    
