    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        
        
        if(nama.getText().equals("") || alamat.getText().equals("") || telepon.getText().equals("")){
         JOptionPane.showMessageDialog(null, "Lengkapi Data");
         return;
         }
        
        String sql;
            if (edit == false) {
                //simpan
                sql = "insert into supplier (nama, alamat, telepon) values ('" + nama.getText() + "', '" + alamat.getText() + "', '"+ telepon.getText()+"')";
            } else {
                sql = "update supplier set nama = '" + nama.getText() + "', alamat = '" + alamat.getText()+"', telepon = '"+telepon.getText() + "' where id_supplier = " + posisi;
            }
        try {
            conn = (Connection) apoteknusantara.koneksi.connect(db);
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
        } catch (Exception e) {JOptionPane.showMessageDialog(null, e.toString());
        }
        
        showData(null);
        clear();
    }//GEN-LAST:event_SaveActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        
        
                if (posisi !=0){
        int response = JOptionPane.showConfirmDialog(this, 
                    "Hapus "+ nama.getText() + " ?",
                    "Perhatian",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.ERROR_MESSAGE);
        
            if(response == JOptionPane.YES_OPTION){
               
               

                try {
                    conn = (Connection) apoteknusantara.koneksi.connect(db);
                    String sql ="delete from supplier where id_supplier =" + posisi;
                    PreparedStatement pst = conn.prepareStatement(sql);
                    pst.execute();
                    conn.close();
                    
                    AddActionPerformed(null);
                    showData(null);
                    JOptionPane.showMessageDialog(this, "Berhasil di hapus");
                } catch (SQLException sQLException) {
                    JOptionPane.showMessageDialog(this, "galat"
                            + "");
                } 
               clear(); 
            }
        }
    }//GEN-LAST:event_DeleteActionPerformed

